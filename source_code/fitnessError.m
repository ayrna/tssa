%% fitnessError
% Function: Calculates the error fitness function
% 
% Input:
%     errors:     Array with the RMSE, RMSEp and MAXe
%     typeError:  Specify the error for the fitness function
%         1 - RMSE
%         2 - RMSEp
%         3 - MAXe
%         
% Output:
%     fitness:    Value of the error fitness function
function [fitness] = fitnessError(errors,typeError)
    if typeError == 1,
        fitness = 1/(1+errors(1));
    elseif typeError == 2,
        fitness = 1/(1+errors(2));
    else
        fitness = 1/(1+errors(3));
    end
end
